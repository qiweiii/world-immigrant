const catalogs: Record<Locale, Record<UiKey, string>> = {
  en,
  "zh-Hans": zhHans,
};

export function t(key: UiKey, locale: Locale): string {
  return catalogs[locale][key] ?? catalogs[defaultLocale][key] ?? key;
}

export function regionLabel(region: string, locale: Locale): string {
  const key = (
    {
      Asia: "region.Asia",
      Europe: "region.Europe",
      "Middle East": "region.MiddleEast",
      "North America": "region.NorthAmerica",
      Oceania: "region.Oceania",
    } as const
  )[region as "Asia" | "Europe" | "Middle East" | "North America" | "Oceania"];
  return key ? t(key, locale) : region;
}

export function tf(
  key: UiKey,
  locale: Locale,
  values: Record<string, string | number> = {},
): string {
  return t(key, locale).replace(/\{([a-zA-Z0-9_]+)\}/g, (match, name: string) =>
    Object.hasOwn(values, name) ? String(values[name]) : match,
  );
}
